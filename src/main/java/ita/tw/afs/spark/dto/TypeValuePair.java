package ita.tw.afs.spark.dto;

public class TypeValuePair {
    private String type;
    private String vale;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getVale() {
        return vale;
    }

    public void setVale(String vale) {
        this.vale = vale;
    }
}
